CREATE TABLE tx_mysearch_domain_model_myindex (

	name varchar(255) DEFAULT '' NOT NULL COMMENT 'name of the index describing its function as  one word',
	vendor varchar(255) DEFAULT '' NOT NULL COMMENT 'vendor of the author of the index',
	index_ident varchar(255) DEFAULT '' NOT NULL COMMENT 'vendor+name = indexIdent ',
	index_score_factor decimal(11,2) DEFAULT 1.00 COMMENT 'from the user added factor for the index',
	title_trans_key text DEFAULT '' NOT NULL COMMENT 'translationkey for the title of the indes',
	description_trans_key text COMMENT 'translationkey for the  description of the index (optinal for internal use) ',
	linkbase_files_folder text COMMENT 'older of import-files with links for the index - helpfully predefined for scheduler',
	default_type varchar(255) DEFAULT '' NOT NULL COMMENT 'defined type of bookmarks - helpfully predefined for scheduler',
	default_grade varchar(255) DEFAULT '' NOT NULL COMMENT 'defined grade of the links in general - helpfully predefined for scheduler',
	login_param text COMMENT 'reserved for flex fields for login-infos currently unused',
	htaccess_param text COMMENT 'reserved for flex fields for htaccess-login-nfos, currently unused ',
	payment_param text COMMENT 'reserved for flex fields for paymentprocesses, currently unused'

);

