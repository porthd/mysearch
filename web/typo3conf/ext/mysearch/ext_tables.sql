CREATE TABLE tx_mysearch_domain_model_linkbase (

	link text COMMENT 'Webpage or absolute filepath to index in searchengine - one link for one index',
	link_hash varchar(255) DEFAULT '' NOT NULL COMMENT '',
	link_score_factor decimal(11,2) DEFAULT '1.00' NOT NULL COMMENT 'individuall score for link - defaultvalue is 1.00',
	index_ident varchar(255) DEFAULT '' NOT NULL '' COMMENT 'unique identifier fur the bas-link for this index',
	index_score_factor decimal(11,2) DEFAULT '1.00' NOT NULL COMMENT 'individuall score for index - defaultvalue is 1.00',
	type varchar(255) DEFAULT '' NOT NULL COMMENT 'distinguish betweene differen type of data - source, thesis, overview, archive, news, chats, logs, ...',
	grade varchar(255) DEFAULT '' NOT NULL COMMENT 'distinguish betweene differen grade of quality - super, good, solid, sufficient, poor, faulty, bad, ..',
	title varchar(255) DEFAULT '' NOT NULL COMMENT 'title given by the expert',
	description text COMMENT 'description  given by the expert - should contain all relevant keywords',
	crawl_deep int(11) DEFAULT '0' NOT NULL COMMENT ' generated by crawl-action, reset by index-deletation',
	crawl_start_link text COMMENT ' generated by crawl-action, reset by index-deletation',
	crawl_connections int(11) DEFAULT '0' NOT NULL COMMENT 'updated by crawl-actions, reset by index-deletation',
	login_param text COMMENT 'reserved for flex fields for login-infos currently unused',
	htaccess_param text COMMENT 'reserved for flex fields for htaccess-login-nfos, currently unused ',
	payment_param text COMMENT 'reserved for flex fields for paymentprocesses, currently unused'

